    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
          jLabel5.setText("" + jSlider1.getValue());
          if(a==0){
            a = Double.parseDouble(jTextField1.getText());
          }
          
          

          DecimalFormat f = new DecimalFormat();
		  f.setMaximumFractionDigits(3);
          jTextField1.setText("" + f.format((double) a*jSlider1.getValue()/100));
          if(Double.parseDouble(jTextField1.getText()) != (double) 0 ){
                  if(!em.isAlive()){
                      ItrafgenApp.droped.set(num,(long)0);
                      ItrafgenApp.nbdroped.set(num,(long)0);
                      ItrafgenApp.launched.set(num, new Boolean(true));
                      em = new Thread(ItrafgenApp.emflux.get(num));
                      em.setPriority(Thread.NORM_PRIORITY);
					  em.start();
                  }
                  debit = Double.parseDouble(jTextField1.getText());
          }
          else{
                    ItrafgenApp.droped.set(num, (long) 0);
                    ItrafgenApp.nbdroped.set(num, (long) 0);
                    ItrafgenApp.launched.set(num, new Boolean(false));
                    ItrafgenApp.resultat.get(num).removeAllElements();
                    ItrafgenApp.graphic.get(num).chart.setTitle("Latence (ms)");
					em.stop();
                em.stop();
            
          }
        
          

    }//GEN-LAST:event_jSlider1StateChanged

