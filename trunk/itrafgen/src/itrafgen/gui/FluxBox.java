public void launch(){
         jSlider1.enable(true);
         jButton3.setEnabled(false);
         jButton4.setEnabled(false);
         jTabbedPane1.setEnabled(false);
         jButton5.setEnabled(true);
         jCheckBox1.setEnabled(false);
                    jTextField1.enable(false);
                    if(jCheckBox4.isSelected()){
						modedemarrage = 1;
					}
					if(jCheckBox3.isSelected()){
						modedemarrage = 2;
					}
					if(jCheckBox5.isSelected()){
						modeenvoi = 1;
					}
                    if(jCheckBox2.isSelected()){
						modeenvoi = 2;
					}
                    ItrafgenApp.droped.set(num,(long)0);
                    ItrafgenApp.nbdroped.set(num,(long)0);
                    ItrafgenApp.launched.set(num, new Boolean(true));
                    debit = Double.parseDouble(jTextField1.getText());
                    if(debit != (double) 0){


					em = new Thread(ItrafgenApp.emflux.get(num));
					em.setPriority(Thread.NORM_PRIORITY);
					em.start();
                    }
                    else{
                       //System.out.println("debit = 0 ; pas de demarrage");
                    }
}

